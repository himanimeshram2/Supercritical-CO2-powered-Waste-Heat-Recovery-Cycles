    Reboiler_T_in_hot = cp.PropsSI('T', 'H', Reboiler_h_in_hot, 'P', Turbocirculator_p_out, fluid_cycle)
    Reboiler_dh = (Reboiler_h_in_hot - Reboiler_h_out_hot_id) * Reboiler_eff
    Reboiler_h_out_hot = Reboiler_h_in_hot - Reboiler_dh
