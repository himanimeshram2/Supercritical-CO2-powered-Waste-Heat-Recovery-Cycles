        Turbine_T_out = cp.PropsSI('T', 'H', Turbine_h_out, 'P', Compressor_p_in, fluid_cycle)
        Turbine_s_out = cp.PropsSI('S', 'T', Turbine_T_out, 'P', Compressor_p_in, fluid_cycle)
        Turbine_power = Turbine_fr * (Turbine_h_in - Turbine_h_out)

